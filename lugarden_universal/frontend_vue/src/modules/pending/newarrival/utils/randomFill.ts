/**
 * 从字符池随机抽取指定长度的字符串
 * @param length 目标长度
 * @returns 随机字符串
 */
export function randomFill(length: number): string {
  let result = ''
  for (let i = 0; i < length; i++) {
    result += charPool[Math.floor(Math.random() * charPool.length)]
  }
  return result
}

/**
 * 对字符串数组中的每个元素按原长度随机填充
 * @param lines 原始行数组（仅用于获取长度）
 * @returns 随机填充后的行数组
 */
export function randomFillLines(lengths: number[]): string[] {
  return lengths.map(len => randomFill(len))
}
